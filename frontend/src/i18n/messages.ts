type MessageShape<T> =
  T extends string ? string
  : T extends (...args: infer A) => infer R ? (...args: A) => R
  : T extends readonly (infer U)[] ? readonly MessageShape<U>[]
  : T extends object ? { [K in keyof T]: MessageShape<T[K]> }
  : T

